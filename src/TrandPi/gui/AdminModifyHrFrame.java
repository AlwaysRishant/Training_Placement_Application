    private void btnmodifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodifyActionPerformed
        if (!validatepwd()) {
            return;
        }
        try{
            boolean result=UserDAO.updatePwd(jcuserid.getSelectedItem().toString(), password);
            if (result) {
                 JOptionPane.showMessageDialog(null, "Password change Succesfully","Succesfull",JOptionPane.INFORMATION_MESSAGE);
            }
            else
            {
                 JOptionPane.showMessageDialog(null, "sorry could not change the Password ","Unsuccesfull",JOptionPane.ERROR_MESSAGE);    
            }
            clearText();
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"DB Error"+ex.getMessage());
            ex.printStackTrace();
        }   
    }//GEN-LAST:event_btnmodifyActionPerformed

