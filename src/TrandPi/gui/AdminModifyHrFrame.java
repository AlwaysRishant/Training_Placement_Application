    private boolean validatepwd()
    {
        char []pwd=txtpwd.getPassword();
        char []cpwd=txtcpwd.getPassword();
        if (pwd.length<=4||cpwd.length<=4) {
            JOptionPane.showMessageDialog(null, "Password must be atleast 4 character or digit","Information",JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        password=String.valueOf(pwd);
        cpassword=String.valueOf(cpwd);
        if(password.equals(cpassword)==false)
        {
             JOptionPane.showMessageDialog(null, "Password not match","Information",JOptionPane.INFORMATION_MESSAGE);
             return false;
        }
        return true;   
    }
