    private void btnaddjobActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddjobActionPerformed
         if(ValidateInput()==false)
         {
             JOptionPane.showMessageDialog(null, "Plzz fill all the fields","Error!",JOptionPane.ERROR_MESSAGE);
             return;
         }
         JobPojo job=new JobPojo();
         job.setJobId(lblJobid.getText().trim());
         job.setJobTitle(txtjobtitle.getText().trim());
         job.setHrId(CurrentUser.getId());
         job.setTags(txt1.getText().trim()+","+txt2.getText().trim()+","+txt3.getText().trim()+","+txt4.getText().trim());
         job.setStatus(0);
         try{
             boolean result=JobDAO.addNewJob(job);
             if (result) {
                 JOptionPane.showMessageDialog(null,"Job Added Succesfull!","Succes",JOptionPane.INFORMATION_MESSAGE);
                 clearText();
                 loadData();
                 return;
             }
             JOptionPane.showMessageDialog(null, "Could not add the job","failure",JOptionPane.ERROR_MESSAGE);
         }
         catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"DB Error"+ex.getMessage());
            ex.printStackTrace(); 
        } 
         
    }//GEN-LAST:event_btnaddjobActionPerformed

