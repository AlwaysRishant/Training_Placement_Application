    private void loadData()
    {
        try{
            lblJobid.setText("Job-"+JobDAO.getNewJobId());
            if (txtcompanyname.getText().isEmpty()) {
                txtcompanyname.setText(hrDAO.getCompanyNameById(CurrentUser.getId()));
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"DB Error"+ex.getMessage());
            ex.printStackTrace(); 
        } 
    }
    private boolean ValidateInput()
    {
        if(txtjobtitle.getText().trim().isEmpty()||txt1.getText().trim().isEmpty()||txt2.getText().trim().isEmpty()||txt3.getText().trim().isEmpty()||txt4.getText().trim().isEmpty())
        {
            return false;
        }            
        return true;        
    }
