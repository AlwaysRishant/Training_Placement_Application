    private void btnnextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnnextActionPerformed
        if (validateInput()) {
            QuestionPojo question=setQuestion();
            if (counter==questionlist.size()) {
                questionlist.add(question);
                clearText();
                ++counter;
            }
            else{
                questionlist.set(counter, question);
                ++counter;
                if (counter==questionlist.size()) {
                    clearText();
                }
                else{
                    getQuestionFromList();
                }
                
            }
            lblqno.setText(String.valueOf(counter+1));
        }
        else{
            JOptionPane.showMessageDialog(null, "Plzz fill all the fields", "Empty question", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnnextActionPerformed

