    private void btnsubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsubmitActionPerformed
        if (validateInput()) 
        {
            QuestionPojo que=setQuestion();
            if (counter==questionlist.size()) {
                questionlist.add(que);
            }
            else{
                questionlist.set(counter, que);
            }
            try{
                boolean result=questionDAO.setPaper(questionlist);
                if (result) {
                    JOptionPane.showMessageDialog(null, "Question paper added succesfully","Succes",JOptionPane.INFORMATION_MESSAGE);
                    JobDAO.setJobStatus(jobId);
                    new HrQuizOptionFrame().setVisible(true);
                    this.dispose();
                }
                else{
                    JOptionPane.showMessageDialog(null,"Could not add the paper","Failure",JOptionPane.WARNING_MESSAGE);
                }
            }
            catch(SQLException ex)
            {
                JOptionPane.showMessageDialog(null,"DB Error","Error in db communication",JOptionPane.WARNING_MESSAGE);
                ex.printStackTrace();
            }
        }  
        else{
            JOptionPane.showMessageDialog(null,"Plzz fill all the fields","Empty questions",JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btnsubmitActionPerformed

    private void btnpreviousActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnpreviousActionPerformed
         if (counter==0) {
            return;
        }
        if (counter==questionlist.size()) 
        {
            if (validateInput()) {
                int result=JOptionPane.showConfirmDialog(null, "you have unsaved question.Do you want to save it","Confirm",JOptionPane.YES_NO_OPTION);
                if (result==JOptionPane.YES_OPTION) {
                    QuestionPojo question=setQuestion();
                    questionlist.add(question);
                    clearText();
                  }
            }
            --counter;
            getQuestionFromList();
        }
        else{
            if (validateInput()) {
                QuestionPojo question=setQuestion();
                questionlist.set(counter, question);
                --counter;
                getQuestionFromList();
            }else{
                JOptionPane.showMessageDialog(null,"plzz fill all the fields","Empty question",JOptionPane.WARNING_MESSAGE);
            }
        }
        lblqno.setText(String.valueOf(counter+1));
    }//GEN-LAST:event_btnpreviousActionPerformed

