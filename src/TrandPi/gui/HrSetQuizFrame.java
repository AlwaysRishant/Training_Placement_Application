    private boolean validateInput()
    {
        if (txtque.getText().isEmpty()||txtoption1.getText().isEmpty()||txtoption2.getText().isEmpty()||txtoption3.getText().isEmpty()||txtoption4.getText().isEmpty()||jcchoosecorrectoption.getSelectedIndex()==0) {
            return false;
        }
        return true;
    }
    private void clearText()
    {
        txtque.setText("");
        txtoption1.setText("");
        txtoption2.setText("");
        txtoption3.setText("");
        txtoption4.setText("");
        jcchoosecorrectoption.setSelectedIndex(0);
        txtque.requestFocus();
    }
    private QuestionPojo setQuestion()
    {
        QuestionPojo question=new QuestionPojo();
        question.setJobId(jobId);
        question.setQno(counter+1); 
        question.setQuestion(txtque.getText().trim());
        question.setOption1(txtoption1.getText().trim());
        question.setOption2(txtoption2.getText().trim());
        question.setOption3(txtoption3.getText().trim());
        question.setOption4(txtoption4.getText().trim());
        question.setCorrectoption(jcchoosecorrectoption.getSelectedIndex());
        return question;
    }
