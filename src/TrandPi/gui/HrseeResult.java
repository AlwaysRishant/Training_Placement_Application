    private void loadAllstudentResult() {
        try{
            studentResult=hrDAO.HrseeResultStudent();
            for(ResultPojo student: studentResult){
                Vector<String> row=new Vector<>();
                row.add(student.getName());
                row.add(student.getpId());
                row.add(student.getJobId());
                row.add(student.getPercentage()+"");
                row.add(student.getUserid());
                row.add(student.getJobTitle());
                model.addRow(row);
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "SQL Error","Db problem",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace(); 
        } 
    }
